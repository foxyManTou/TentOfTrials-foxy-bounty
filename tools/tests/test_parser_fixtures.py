def test_aggregator_integration():
    """Test that LogAggregator correctly processes all fixture types."""
    aggregator = LogAggregator()

    # Write fixture lines to a temp file and process it
    all_lines = []
    all_lines.extend(f[0] for f in JSON_FIXTURES if f[1] is not None)
    all_lines.extend(f[0] for f in TEXT_FIXTURES if f[1] is not None)
    all_lines.extend(f[0] for f in NGINX_FIXTURES if f[1] is not None)

    with tempfile.NamedTemporaryFile(mode="w", suffix=".log", delete=False) as f:
        f.write("\n".join(all_lines) + "\n")
        temp_path = f.name

    try:
        count = aggregator.process_file(temp_path)
        summary = aggregator.get_summary()

        # Verify counts
        expected_valid = (
            sum(1 for f in JSON_FIXTURES if f[1] is not None)
            + sum(1 for f in TEXT_FIXTURES if f[1] is not None)
            + sum(1 for f in NGINX_FIXTURES if f[1] is not None)
        )
        assert count == expected_valid, (
            f"Expected {expected_valid} parsed entries, got {count}"
        )
        assert summary["total_entries"] == expected_valid, (
            f"Summary mismatch: expected {expected_valid}, got {summary['total_entries']}"
        )

        # Verify level counts are populated
        assert len(summary["by_level"]) > 0, "by_level should not be empty"
        assert summary["error_rate"] >= 0, "error_rate should be >= 0"

        print(f"  ✓ Aggregator integration: {count} entries processed correctly")
        print(f"    Levels: {summary['by_level']}")
        print(f"    Services: {summary['by_service']}")
        print(f"    Error rate: {summary['error_rate']}%")
    finally:
        os.unlink(temp_path)


def test_malformed_lines_dont_crash():
    """Test that malformed lines don't crash parsing."""
    parser = JSONLogParser()
    malformed_lines = [
        '{"broken": "json"',
        "{not json at all}",
        "just a plain string",
        "",
        "   ",
        "null",
        "12345",
    ]
    for line in malformed_lines:
        try:
            result = parser.parse(line)
            # Should return None, not crash
        except Exception as e:
            assert False, f"JSONLogParser crashed on line {repr(line)}: {e}"

    parser = TextLogParser()
    for line in malformed_lines:
        try:
            result = parser.parse(line)
        except Exception as e:
            assert False, f"TextLogParser crashed on line {repr(line)}: {e}"

    parser = NginxLogParser()
    for line in malformed_lines:
        try:
            result = parser.parse(line)
        except Exception as e:
            assert False, f"NginxLogParser crashed on line {repr(line)}: {e}"

    print(f"  ✓ Malformed lines: all parsers handle {len(malformed_lines)} crash cases gracefully")



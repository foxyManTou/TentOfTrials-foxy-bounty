def test_json_parser():
    """Test JSONLogParser against hand-written fixtures."""
    parser = JSONLogParser()
    for line, expected in JSON_FIXTURES:
        result = parser.parse(line)
        if expected is None:
            assert result is None, f"Expected None for line: {line[:60]}, got: {result}"
        else:
            assert result is not None, f"Expected result for line: {line[:60]}"
            for key, val in expected.items():
                if key == "timestamp":
                    continue  # timestamp format varies; skip exact match
                assert result.get(key) == val, (
                    f"Mismatch for key '{key}' on line: {line[:60]}\n"
                    f"  Expected: {val}\n"
                    f"  Got: {result.get(key)}"
                )
    print(f"  ✓ JSONLogParser: {len([e for e in JSON_FIXTURES if e[1] is not None])} valid, "
          f"{len([e for e in JSON_FIXTURES if e[1] is None])} malformed cases passed")


